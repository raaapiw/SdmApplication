        kerja ditetapkan berakhir dan <b>tidak membayar</b> ganti rugi kepada <b>PIHAK
        PERTAMA</b> sebagaimana diatur dalam ayat (3) diatas, maka :</span></p>
        
        <p class=MsoNormal style='margin-left:36.0pt;text-align:justify;text-indent:
        -18.0pt'><span lang=ES-CR style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>a.<span
        style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp; </span></span><b><span
        lang=ES-CR style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>PIHAK KEDUA</span></b><span
        lang=ES-CR style='font-size:11.0pt;font-family:"Tahoma",sans-serif'> tidak
        diberikan Surat Keterangan Kerja.</span></p>
        
        <p class=MsoNormal style='margin-left:36.0pt;text-align:justify;text-indent:
        -18.0pt'><span lang=ES-CR style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>b.<span
        style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp; </span></span><span
        lang=ES-CR style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>Diterbitkan
        surat Pemberhentian bahwa <b>PIHAK KEDUA</b> mengundurkan diri secara tidak
        baik karena tidak memenuhi kewajibannya sebagaimana yang telah  disepakati
        (wanprestasi).</span></p>
        
        <p class=MsoNormal style='margin-left:36.0pt;text-align:justify'><span
        lang=ES-CR style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>&nbsp;</span></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif;color:black'>Pasal 8</span></b></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span lang=FI
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>BERAKHIRNYA PERJANJIAN
        KERJA</span></b></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span lang=FI
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>&nbsp;</span></b></p>
        
        <p class=MsoNormal style='text-align:justify'><span lang=FI style='font-size:
        11.0pt;font-family:"Tahoma",sans-serif'>Bila hubungan kerja ini putus demi
        hukum karena berakhirnya masa perjanjian kerja, maka bila perjanjian kerja ini
        tidak dilanjutkan dengan suatu hubungan kerja baru, maka perjanjian kerja ini
        akan berakhir secara otomatis dan <b>PIHAK KEDUA</b> wajib menyerahkan semua
        hal yang dipercayakan perusahaan kepada <b>PIHAK PERTAMA</b>.</span></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif;color:black'>Pasal  9</span></b></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>FORCE MAJEURE</span></b></p>
        
        <p class=MsoNormal><b><span style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>&nbsp;</span></b></p>
        
        <p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
        -9.0pt'><span style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>1. Yang
        dimaksud dengan Force Majeure dalam perjanjianini adalah keadaan-keadaan
        sebagai berikut :</span></p>
        
        <p class=MsoNormal style='margin-left:40.5pt;text-align:justify;text-indent:
        -22.5pt'><span style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>a.<span
        style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>Gempa bumi besar,
        taufan, banjir besar, tanah longsor, wabah penyakit dan bencana alam;</span></p>
        
        <p class=MsoNormal style='margin-left:40.5pt;text-align:justify;text-indent:
        -22.5pt'><span style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>b.<span
        style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>Pemberontakan, pemogokan
        umum, huru hara, sabotase, perang dan kebijaksanaan pemerintah yang berakibat
        langsung terhadap perjanjian ini. </span></p>
        
        <p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
        -18.0pt'><span style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>2. 
        Dalam hal terjadi Force Majeure sebagaimana yang dimaksud pada ayat (1) pasal
        ini, maka pihak yang terkena wajib memberitahukan secara tertulis kepada pihak
        lainnya dalam waktu 7 (tujuh) hari kalender sejak diterima pemberitahuan oleh
        pihak lainnya dimana adanya Force Majeure tersebut diterangkan secara resmi
        oleh pejabat pemerintah yang berwenang.</span></p>
        
        <p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
        -18.0pt'><span style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>3.  Kelalaian
        atau keterlambatan dalam memenuhi kewajiban memberitahukan sebagaimana dimaksud
        pada ayat (2) pasal ini, mengakibatkan tidak diakuinya oleh pihak lainnya
        peristiwa dimaksud pada ayat (1) pasal ini sebagai Force Majeure.</span></p>
        
        <p class=MsoNormal style='margin-left:18.0pt;text-align:justify;text-indent:
        -18.0pt'><span lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>4.  Akibat
        adanya Force Majeure, maka <b>PIHAK PERTAMA </b>dibebaskan dari pembayaran sisa
        kontrak <b>PIHAK KEDUA</b>.</span></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif;color:black'>Pasal  10</span></b></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span lang=SV
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>PENYELESAIAN
        PERSELISIHAN</span></b></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif;color:black'>&nbsp;</span></b></p>
        
        <p class=MsoNormal style='margin-left:20.0pt;text-align:justify;text-indent:
        -20.0pt'><span lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>1.<span
        style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
        lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>Apabila
        kemudian hari terjadi perselisihan dalam penafsiran atau pelaksanaan
        ketentuan-ketentuan dari perjanjian kerja ini, kedua belah pihak setuju untuk
        menyelesaikannya secara musyawarah dan mufakat.</span></p>
        
        <p class=MsoNormal style='margin-left:20.0pt;text-align:justify;text-indent:
        -20.0pt'><span lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>2.<span
        style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
        lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>Apabila dengan
        cara musyawarah dan mufakat, perselisihan tidak dapat diselesaikan, maka kedua
        belah pihak sepakat untuk menyelesaikannya sesuai dengan per undang-undangan
        yang berlaku.</span></p>
        
        <p class=MsoNormal align=center style='text-align:center'><span lang=SV
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif;color:black'>&nbsp;</span></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span lang=SV
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif;color:black'>Pasal 11</span></b></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span lang=SV
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>LAIN-LAIN</span></b></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span lang=SV
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>&nbsp;</span></b></p>
        
        <p class=MsoNormal style='margin-left:20.0pt;text-align:justify;text-indent:
        -18.0pt'><span lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>1.<span
        style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp; </span></span><b><span
        lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>PIHAK PERTAMA </span></b><span
        lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>tidak menanggung
        biaya perpanjangan sertifikat dan tidak memberikan jaminan adanya pendidikan
        sesuai dengan kualifikasi.</span></p>
        
        <p class=MsoNormal style='margin-left:20.0pt;text-align:justify;text-indent:
        -18.0pt'><span lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>2.<span
        style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp; </span></span><span
        lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>Selama
        berlangsungnya perjanjian kerja ini <b>PIHAK KEDUA </b>dilarang bekerja,
        memberi informasi milik perusahaan, menggunakan fasilitas perusahaan, membocorkan
        rahasia perusahaan pada pihak lain tanpa persetujuan tertulis terlebih dahulu
        dari <b>PIHAK PERTAMA</b>.</span></p>
        
        <p class=MsoNormal style='margin-left:20.0pt;text-align:justify;text-indent:
        -18.0pt'><span lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>3.<span
        style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp; </span></span><span
        lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>Pada saat
        ditandatanganinya dan berlakunya perjanjian ini, <b>PIHAK KEDUA </b>tidak
        sedang terikat perjanjian kerja dengan pihak lainnya.</span></p>
        
        <p class=MsoNormal align=center style='margin-left:17.85pt;text-align:center;
        text-indent:-17.85pt'><span style='font-size:11.0pt;font-family:"Tahoma",sans-serif;
        color:black'>&nbsp;</span></p>
        
        <p class=MsoNormal align=center style='margin-left:17.85pt;text-align:center;
        text-indent:-17.85pt'><b><span style='font-size:11.0pt;font-family:"Tahoma",sans-serif;
        color:black'>Pasal 12</span></b></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>PENUTUP</span></b></p>
        
        <p class=MsoNormal align=center style='text-align:center'><b><span
        style='font-size:11.0pt;font-family:"Tahoma",sans-serif'>&nbsp;</span></b></p>
        
        <p class=MsoNormal style='margin-left:17.85pt;text-align:justify;text-indent:
        -17.85pt'><span lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif;
        color:black'>1.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp; </span></span><span
        lang=SV style='font-size:11.0pt;font-family:"Tahoma",sans-serif;color:black'>Dengan
